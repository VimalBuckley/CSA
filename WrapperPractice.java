public class WrapperPractice {
    

    public static void main(String[] args) {
        Integer int1 = new Integer(5); // Integer constructor
        int primativeInt = int1.intValue(); // intValue method
        Double double1 = new Double(5.0); // Double constructor
        double primativeDouble = double1.doubleValue(); // doubleValue method
        double max = Double.MAX_VALUE; // Max double value
        double min = Double.MIN_VALUE; // Min double value
        Double autoBoxed = 5.0; // Autoboxing
        double unboxed = new Double(5.0); // Unboxing
    }
}
